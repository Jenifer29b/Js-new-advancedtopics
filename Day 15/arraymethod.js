// push is method in array is used to add one or more elements in end of an array
//pop is used to delete an last elemnent in an array

let arr1 = ["apple", "mango", "kiwi"]
console.log(arr1)
arr1.push("banana","papaya")
console.log(arr1)
arr1.pop()
console.log(arr1)

//unshift is used to add elements in starting of an array
// shift is used to remove first element in an array
let arr2 = ["car", "bus", "bike"]
arr2.unshift("lorry","truck")
console.log(arr2)
arr2.shift()
console.log(arr2)